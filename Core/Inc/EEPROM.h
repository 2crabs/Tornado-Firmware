#ifndef INC_EEPROM_H_
#define INC_EEPROM_H_

#define EEPROM_ADDR (0b1010000 << 1)
#define EEPROM_SET_PAGE0 (0b0110110 << 1)
#define EEPROM_SET_PAGE1 (0b0110111 << 1)
#define EEPROM_READ_PAGE (0b0110110 << 1)

#define EEPROM_BOARD_VERSION 0x00

#define EEPROM_FIRMWARE_VERSION 0x04

#define EEPROM_CAN_ID 0x16
#define EEPROM_PERIODIC_ENABLE 0x17
#define EEPROM_PERIODIC_RATE 0x18

#define EEPROM_LED_R 0x20
#define EEPROM_LED_G 0x21
#define EEPROM_LED_B 0x22

#define EEPROM_ACCEL_ENABLE 0x28
#define EEPROM_ACCEL_ODR 0x29
#define EEPROM_ACCEL_SCALE 0x30

#define EEPROM_GYRO_ENABLE 0x32
#define EEPROM_GYRO_ODR 0x33
#define EEPROM_GYRO_SCALE 0x34

#define EEPROM_SFLP_ENABLE 0x36
#define EEPROM_SFLP_ODR 0x37
#define EEPROM_SFLP_TYPE 0x38

#endif /* INC_EEPROM_H_ */
